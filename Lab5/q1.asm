	AREA RESET,DATA,READONLY
	EXPORT __Vectors

__Vectors

	DCD 0X10000100
	DCD Reset_Handler
	ALIGN
	AREA MY_CODE,CODE,READONLY
	ENTRY
	EXPORT Reset_Handler

Reset_Handler

	MOV R0, #3; Array size
	LDR R1, =SRC; R1 <- SRC
	LDR R8, [R1], #4;
	LDR R9, =DST;

OUTER_LOOP
	LDR R2, [R1], #4;
	CMP R0, #0
	BNE INNER_LOOP
	SUBS R0, #1
	CMP R0, #10
	BNE OUTER_LOOP

LOW
	LDR R4, [R5]; R4 HOLDS THE LOWEST ELEMENT 
	
SWAP
	LDR R7, [R4]
	LDR R4, [R2]
	LDR R2, [R7]	
	

INNER_LOOP
	LDR R5, [R1]	
	LDR R3, [R0]
	
	
BACK
	SUB R3, #1
	LDR R6, [R5], #4
	CMP R2, R5
	BLO LOW
	CMP R3, #0
	BNE BACK
	CMP R5, R4
	BLO SWAP
	BHI OUTER_LOOP
	

STOP B STOP
SRC DCD 0x14, 0X13, 0X12

	AREA MY_DATA,DATA,READWRITE

DST DCD 0
	END